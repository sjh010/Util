package com.mobileleader.rpa.view.data.dto;

public class TaskStatusCodeInfo {

    private String taskStatusCode;

    private String taskStatusCodeName;

    public TaskStatusCodeInfo() {
        super();
    }

    public String getTaskStatusCode() {
        return taskStatusCode;
    }

    public void setTaskStatusCode(String taskStatusCode) {
        this.taskStatusCode = taskStatusCode;
    }

    public String getTaskStatusCodeName() {
        return taskStatusCodeName;
    }

    public void setTaskStatusCodeName(String taskStatusCodeName) {
        this.taskStatusCodeName = taskStatusCodeName;
    }


}
